    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        // TODO add your handling code here:
        //sea crean las variables que voy a utilizar en el boton de guardar
        String Errores = "";
        String run = txtRun.getText();
        String nombre = txtNombre.getText();
        String apellidos = txtApellidos.getText();
        int edad = 0;
        // aqui valide que la edad sea numerica, por eso se utilizo try-catch
        try {
            edad = Integer.parseInt(txtEdad.getText());
        } catch (NumberFormatException e) {
            Errores += edad;
        }
        // aqui se utiliza un jcombobox para listar las nacionalidades 
        String nacionalidad = txtNacionalidad.getSelectedItem().toString();
        // aqui se utiliza un jcombobox 
        String visa = comboVisa.getSelectedItem().toString();
        // aqui se utiliza un jtextfield
        String patente = txtPatente.getText();
        // aqui se utiliza un jcombobox
        String tipo = comboTipo.getSelectedItem().toString();
        // aqui se validan los errores a traves del if's
        if ("SELECCIONE VEHICULO".equals(tipo)) {
            Errores += tipo;
        }
        if (nacionalidad != "CHILENO" && visa == "SELECCIONAR") {
            Errores += " " + visa;
        } else if (nacionalidad == "CHILENO" && visa != "SELECCIONAR") {
            Errores += visa;
        }
        if (!fun.validarRut(run)) {
            Errores += run;
        }
        // aqui se utiliza un jtextfield
        String color = txtColor.getText();
        String marca = txtMarca.getText();
        String hora = txtHora.getText();
        // validamos que el largo de cadena sea menor a 2 y que le agruegue un cero antes (0) si es 1 , ya que ocuparemos la variable como cadena(string) 
        if (hora.length() < 2) {
            hora = "0" + hora;
        }
        // validamos que el largo de cadena sea menor a 2 y que le agruegue un cero antes (0) si es 1 , ya que ocuparemos la variable como cadena(string) 
        String minuto = txtMinutos.getText();
        if (minuto.length() < 2) {
            minuto = "0" + minuto;
        }
        // se concatena la hora y minuto
        String entrada = hora + ":" + minuto;
        // aqui validamos que la variable errores este vacia para poder guardar
        if (Errores.isEmpty()) {
            //se inicializa la clase parquimetro con el constructor con parametros
            Parquimetro parking = new Parquimetro(patente, tipo, color, marca, run, nombre, apellidos, edad, nacionalidad, visa, entrada);
            //agregamos el objeto parquimetro a la lista de modelo (defaultmodellist) con la funcion addelement
            // su indice empieza de cero
            Modelo1.addElement(parking);
            //aqui volvemos agregar el objeto parquimetro al arraylist<class> por la funcion add
            //su indice empieza de 1
            Modelo.add(parking);
            //setteamos el modelo1, que seria la lista de modelo, ya que el modelo de arreglo de lista no es soportado por la jlist
            Lista.setModel(Modelo1);
        } else {
            //muestra los errores de la variable error en caso de haberlos, en un mensaje de dialogo
            JOptionPane.showMessageDialog(rootPane, "*Revise su Selecci√≥n de vehiculo.\n"
                    + "*Revise la Selecci√≥n de Visa." + "\n*Revise que el Run est√© Correcto."
                    + "\n*Revise que la Edad este en Formato n√∫merico.", "ATENCI√ìN!", JOptionPane.WARNING_MESSAGE);
        }
        //incrementa los numeros de registro, tomando el tama√±o del arraylist<class>
        jTitle.setText("REGISTRO DE VEHICULOS RATA PARQ: " + Modelo.size() + " Vehiculos Aparcados.");
    }//GEN-LAST:event_btnGuardarActionPerformed


    private void btnCobrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCobrarActionPerformed
        // nuevemente validamos que la lista no este vacia y a partir de su indice obtenemos una instancia
        //de la clase parquimetro de la cual podemos obtener los datos a traves de los getters
        if (!Lista.isSelectionEmpty()) {
            Parquimetro par = Modelo.get(Lista.getSelectedIndex());
            // aqui se toma la hora del sistema para calcular la hora de salida y realizar cobro
            // no se puso la opcion de eliminar inmediatamente al realizar el cobro, ya que todas las aplicaciones(capa 8) pueden presentar error
            //asi que se opto por eliminar un registro al hacer click
            LocalTime salida = LocalTime.now();
            int hora = salida.getHour();
            String h = String.valueOf(hora);
            if (hora < 10) {
                h = "0" + hora;
            } else {
                h = "" + hora;
            }
            int min = salida.getMinute();
            String m = String.valueOf(min);
            if (min < 10) {
                m = "0" + min;
            } else {
                m = "" + min;
            }
            int h_s = fun.calcularMinutos(par.getHora_entrada(), h + ":" + m);
            int valor = Integer.parseInt(txtValor.getText());
            int cobro = h_s * valor;
            String mensaje = "El ";
            //SELECCIONE VEHICULO, AUTOMOVIL, BUS, CAMIONETA, CAMION, FURGON, MOTOCICLETA
            if (par.getTipo().equals("MOTOCICLETA") || par.getTipo().equals("CAMIONETA")) {
                mensaje = "La " + par.getTipo() + "\nPlaca Patente" + par.getPlaca() + "\nDebe Pagar: $" + cobro + ".-"
                        + "\n por " + h_s + " minutos.";
            } else {
                mensaje += par.getTipo() + "\nPlaca Patente" + par.getPlaca() + "\nDebe Pagar: $" + cobro + ".-"
                        + "\n por " + h_s + " minutos.";
            }
            // se muestran los datos necesario para el cobro
            JOptionPane.showMessageDialog(rootPane, mensaje);
        } else { //vacio
            JOptionPane.showMessageDialog(rootPane, "No Existen Registros, \nDebe Agregar Datos para Poder Seleccionar la Lista!", "ATENCI√ìN!", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_btnCobrarActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        // TODO add your handling code here:

        //validamos que la lista no este vacia y removemos el indice retornado de los modelos
        if (!Lista.isSelectionEmpty()) {
            Modelo1.remove(Lista.getSelectedIndex());
            Modelo.remove(Lista.getSelectedIndex() + 1);
            Lista.setModel(Modelo1);
            JOptionPane.showMessageDialog(rootPane, "Se ha Eliminado un Registro!\n");
        } else {
            JOptionPane.showMessageDialog(rootPane, "No Existen Registros, \nDebe Agregar Datos para Poder Seleccionar la Lista!", "ATENCI√ìN!", JOptionPane.WARNING_MESSAGE);
        }
        //decrementa/dismuye/elimina los numeros de registro, tomando el tama√±o del arraylist<class>
        jTitle.setText("REGISTRO DE VEHICULOS RATA PARQ: " + Modelo.size() + " Vehiculos Aparcados.");

    }//GEN-LAST:event_btnEliminarActionPerformed

